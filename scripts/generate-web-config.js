#!/usr/bin/env node
/**
 * Генерирует frontend/web/config.js из переменной окружения API_URL.
 * Vercel: Project Settings → Environment Variables → API_URL=https://xxx.up.railway.app
 */
const fs = require('fs');
const path = require('path');

const url = (process.env.API_URL || process.env.VITE_API_URL || '').trim().replace(/\/$/, '');
const outPath = path.join(__dirname, '..', 'frontend', 'web', 'config.js');

const content = `// Auto-generated by scripts/generate-web-config.js
// Vercel: задайте переменную API_URL = https://your-app.up.railway.app
window.DEFAULT_API_URL = ${JSON.stringify(url)};
`;

fs.writeFileSync(outPath, content, 'utf8');
console.log(
  url
    ? `[generate-web-config] DEFAULT_API_URL = ${url}`
    : '[generate-web-config] API_URL не задан — на проде укажите API_URL в Vercel или отредактируйте config.js'
);
